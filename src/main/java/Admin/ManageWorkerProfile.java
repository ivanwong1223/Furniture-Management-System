    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String search = txtSearch.getText();
        DefaultTableModel table = (DefaultTableModel)jTable1.getModel();
        TableRowSorter<DefaultTableModel> tData = new TableRowSorter<>(table);
        jTable1.setRowSorter(tData);
        tData.setRowFilter(new RowFilter<DefaultTableModel, Object>() {
        @Override
        public boolean include(Entry<? extends DefaultTableModel, ? extends Object> entry) {
            // Add your custom conditions here
            // For example, filter based on ID (assuming ID is in the first column)
            String id = entry.getStringValue(0);
            return id.startsWith(search);
        }
    });
    }//GEN-LAST:event_txtSearchKeyReleased

