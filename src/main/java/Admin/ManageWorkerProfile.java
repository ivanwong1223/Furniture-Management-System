    private void btnViewProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProfileActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow>=0){ //if table has data
            String[] rowData = new String[jTable1.getModel().getColumnCount()];
            for (int i = 0; i < jTable1.getModel().getColumnCount(); i++) {
                rowData[i] = (String) jTable1.getModel().getValueAt(selectedRow, i); //fetch all the selected data in that row
            }
            
            // create a new window to display the selected row data
            // pass "this" frame (ManageWorkerProfile) as a listener to WorkerProfilePage
            WorkerProfilePage a = new WorkerProfilePage(rowData, this, ls);
            a.setVisible(true);
            a.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a staff");
        }
    }//GEN-LAST:event_btnViewProfileActionPerformed

