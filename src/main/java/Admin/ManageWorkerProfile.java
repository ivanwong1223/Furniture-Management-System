    public void populateTable() {
    String[] columns = {"ID", "Name", "Password", "Role", "Contact Number", "Email", "Status"};
    DefaultTableModel model = new DefaultTableModel(columns, 0){
    @Override
    public boolean isCellEditable(int row, int column){
        return false;
    }
};
    
    //bufferReader reads data faster and efficient only
    try (BufferedReader br = new BufferedReader(new FileReader("user.txt"))) { //read file
        String line;
        while ((line = br.readLine()) != null) { //if file is not empty
            String[] data = line.split(", "); 
            // Check if the role is not "admin" before adding data to the table
            if (!data[3].equals("admin")) {
                model.addRow(data); //add data to table
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    
    jTable1.setModel(model);
    jTable1.setRowSelectionAllowed(true);
    jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
}

