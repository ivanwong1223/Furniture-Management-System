    private void printBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printBtnActionPerformed
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Save PDF");
    fileChooser.setAcceptAllFileFilterUsed(false);
    
    // Set the default directory to the user's home directory or any specific path
    fileChooser.setCurrentDirectory(new java.io.File(System.getProperty("user.home")));
    
    // Filter only PDF files
    FileNameExtensionFilter pdfFilter = new FileNameExtensionFilter("PDF Documents", "pdf");
    fileChooser.addChoosableFileFilter(pdfFilter);
    fileChooser.setFileFilter(pdfFilter);

    while (true) {
        int userSelection = fileChooser.showSaveDialog(null);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            java.io.File fileToSave = fileChooser.getSelectedFile();
            String filePath = fileToSave.getAbsolutePath();
            // Ensure the file has a ".pdf" extension
            if (!filePath.toLowerCase().endsWith(".pdf")) {
                filePath += ".pdf";
            }
           
            GeneratePDF generator = new GeneratePDF();
            try {
                generator.panelToPDF(invoicePanel, filePath); // Use the selected file path for PDF generation
                JOptionPane.showMessageDialog(null, "PDF generated successfully and saved to: " + filePath, "Success", JOptionPane.INFORMATION_MESSAGE);
                System.out.println("PDF generated successfully.");
                break; // Exit the loop after successful operation
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Failed to generate PDF.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // User cancelled the operation
            JOptionPane.showMessageDialog(null, "PDF save operation was cancelled.", "Cancelled", JOptionPane.WARNING_MESSAGE);
            break; // Exit the loop if the operation was cancelled
        }
    }
    }//GEN-LAST:event_printBtnActionPerformed

