    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        String InvoiceID = invoiceID.getText();
        String customerName = cusName.getText();
        String customerContact = cusContact.getText();
        String customerAddress = cusAddress.getText();
        String salesPersonID = SalesPersonID.getText();
        String salesDate = Date.getText();
        String salesTotal = total.getText();

        DefaultTableModel tableModel = (DefaultTableModel) InvoiceTable.getModel();
        List<String> salesIDs = new ArrayList<>();
        List<String> items = new ArrayList<>();
        List<String> quantities = new ArrayList<>();

        for (int row = 0; row < tableModel.getRowCount(); row++) {
            salesIDs.add(tableModel.getValueAt(row, 0).toString());
            items.add(tableModel.getValueAt(row, 1).toString());
            quantities.add(tableModel.getValueAt(row, 2).toString());
        }

        boolean update = displaySalesInvoice.writeSalesInvoice(InvoiceID, customerName, customerContact, customerAddress, salesPersonID, salesDate, salesTotal, salesIDs, items, quantities);
        ApproveSales a = new ApproveSales(ls);
        a.setVisible(true);
        this.setVisible(false);        
    }//GEN-LAST:event_SubmitButtonActionPerformed

